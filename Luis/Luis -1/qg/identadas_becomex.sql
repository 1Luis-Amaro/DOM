              WHEN fmd.scale_type = 0 THEN
                 (to_number(fmd.qty) / decode(to_number(1),0,1,to_number(1))) / decode(vr.std_qty,0,1,vr.std_qty)
              WHEN fmd.scale_type = 1 THEN
                 (to_number(fmd.qty) / decode(to_number(1),0,1,to_number(1)))
              ELSE
                 (to_number(fmd.qty) / decode(to_number(1),0,1,to_number(1)))
           END qty ,
          CASE
              --WHEN msip.planning_make_buy_code  = 1 THEN
              --   1
              WHEN fmd.scale_type = 0 THEN
                 ((to_number(fmd.qty) / decode(to_number(fm.qty),0,1,to_number(fm.qty))) / decode(vr.std_qty,0,1,vr.std_qty)) / (1 - (vr.planned_process_loss / 100))
              WHEN fmd.scale_type = 1 THEN
                 (to_number(fmd.qty) / decode(to_number(fm.qty),0,1,to_number(fm.qty))) / (1 - (NVL(vr.planned_process_loss,0) / 100))
              ELSE
                 (to_number(fmd.qty) / decode(to_number(fm.qty),0,1,to_number(fm.qty))) / (1 - (NVL(vr.planned_process_loss,0) / 100))
           END qty_cst,   
           fmd.formula_id,
           gr.recipe_id,
           fmd.organization_id,
           fmd.organization_id,
           0 as Nivel
      FROM apps.fm_form_mst_b fms,
